// using a for loop
var sum_to_n_a = function (n) {
  let sum = 0;

  for (let i = 1; i <= n; i++) {
    sum = sum + i;
  }

  return sum;
};

// using mathematical formula
var sum_to_n_b = function (n) {
  let sum = 0;
  sum = (n * (n + 1)) / 2;

  return sum;
};

// using recursion
var sum_to_n_c = function (n) {
  if (n <= 1) return n;
  sum = n + sum_to_n_c(n - 1);

  return sum;
};
